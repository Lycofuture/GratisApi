	getLunarMonths(y) {
		var month = [];
		var c1 = '一|二|三|四|五|六|七|八|九|十|冬|腊'.split('|');
		if(this.getLeapMonth(y) > 0){
			var num = this.getLeapMonth(y);
		}else{
			var num = 13;
		}
		
		for (var i = 1; i <= 12; i++) {
			
			if(i > num ){
				month.push({
					id: i+1,
					value: c1[i - 1] + '月'
				})
				
			}else{
				month.push({
					id: i,
					value: c1[i - 1] + '月'
				})
			}
			
		}
		
		if (num >0 && num < 13) {
			month.splice(num, 0, {
				id: num + 1,
				value: '闰' + c1[num - 1] + '月'
			});
		}
		return month;
	}

	getLunarDayCount(y, m) {
		var days = [];
		var num = this.getDaysByLunarMonth(y, m);
		for (var i = 1; i <= num; i++) {
			days.push({
				id: i,
				value: this.getLunarDayName(i)
			})
		}
		return days;
	}
	getLunarDayName(day) {
		var a = Math.floor(day / 10);
		return map.lunar.c2[day > 10 ? a : 0] + map.lunar.c1[(day - 1) % 10]
	}

